# pico_project/font.py - Basic 8x8 Bitmap Font

# Each character is represented by 8 bytes (8 rows of 8 pixels).
# A '1' bit means foreground color, '0' means background color.

FONT_8X8 = {
    # ASCII: 32 (Space)
    ord(' '): [0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00],
    # ASCII: 48 ('0')
    ord('0'): [0x7C, 0xC6, 0xCE, 0xDE, 0xD6, 0xC6, 0x7C, 0x00],
    # ASCII: 49 ('1')
    ord('1'): [0x30, 0x70, 0x30, 0x30, 0x30, 0x30, 0xFC, 0x00],
    # ASCII: 50 ('2')
    ord('2'): [0x7C, 0xC6, 0x06, 0x3C, 0x60, 0xC0, 0xFE, 0x00],
    # ASCII: 65 ('A')
    ord('A'): [0x30, 0x78, 0xCC, 0xCC, 0xFC, 0xCC, 0xCC, 0x00],
    # ASCII: 66 ('B')
    ord('B'): [0xFC, 0x66, 0x66, 0x7C, 0x66, 0x66, 0xFC, 0x00],
    # ASCII: 67 ('C')
    ord('C'): [0x3C, 0x66, 0xC0, 0xC0, 0xC0, 0x66, 0x3C, 0x00],
    # ASCII: 62 ('>')
    ord('>'): [0x00, 0x0C, 0x18, 0x30, 0x18, 0x0C, 0x00, 0x00],
    # Default character (e.g., block) for missing glyphs
    ord('?'): [0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF],
}

FONT_WIDTH = 8
FONT_HEIGHT = 8

def get_char_bytes(char):
    """Get the 8-byte bitmap for a character, or a default if not found."""
    return FONT_8X8.get(ord(char), FONT_8X8[ord('?')]) 